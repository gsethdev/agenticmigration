_FONT_SECTION = textwrap.dedent("""\

    # ---------------------------------------------------------------
    # Custom Fonts
    # ---------------------------------------------------------------
    Write-Log "Installing custom fonts..."
    $fontsDir = "C:\\\\Windows\\\\Fonts"
    $fontFiles = Get-ChildItem -Path "$PSScriptRoot\\\\fonts" -Include *.ttf, *.otf -Recurse -ErrorAction SilentlyContinue

    foreach ($font in $fontFiles) {
        $destPath = Join-Path $fontsDir $font.Name
        if (-not (Test-Path $destPath)) {
            Copy-Item $font.FullName $destPath -Force
            # Register the font
            $regPath = "HKLM:\\\\SOFTWARE\\\\Microsoft\\\\Windows NT\\\\CurrentVersion\\\\Fonts"
            New-ItemProperty -Path $regPath -Name "$($font.BaseName) (TrueType)" -Value $font.Name -PropertyType String -Force
            Write-Log "Installed font: $($font.Name)"
        }
    }
    Write-Log "Font installation complete."
""")

