"""generate_install_script - Generate install.ps1 for Managed Instance on App Service.

Produces a PowerShell install script for OS-level feature enablement and
configuration on Managed Instance. This script is ONLY for scenarios requiring:
  - COM component registration / MSI installation
  - SMTP server configuration
  - MSMQ client configuration
  - Custom font installation
  - Crystal Reports runtime
  - Other Windows features that need OS-level enablement

Registry keys and local storage / file I/O dependencies should NOT be
handled via install.ps1. Use the generate_adapter_arm_template tool instead
to provision registry adapters (Key Vault-backed) and storage adapters
(Azure Files mounts, local attached storage, or custom storage over VNET).
"""

import json
import os
import textwrap

from tools import server


# Script section templates based on Managed Instance example scripts
_SCRIPT_HEADER = textwrap.dedent("""\
    #######################################################################
    # install.ps1 — Managed Instance on App Service installation script
    #
    # This script runs during app startup on the Managed Instance.
    # It installs Windows features and configures OS-level dependencies
    # required by the application.
    #
    # Generated by IIS Migration MCP Server
    #######################################################################

    $ErrorActionPreference = "Stop"
    $logFile = "D:\\\\home\\\\LogFiles\\\\install_$(Get-Date -Format 'yyyyMMdd_HHmmss').log"

    function Write-Log {
        param([string]$Message)
        $timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
        $entry = "[$timestamp] $Message"
        Add-Content -Path $logFile -Value $entry
        Write-Host $entry
    }

    Write-Log "Starting Managed Instance installation script..."
""")

_SMTP_SECTION = textwrap.dedent("""\

    # ---------------------------------------------------------------
    # SMTP Server Feature
    # ---------------------------------------------------------------
    Write-Log "Installing SMTP Server feature..."
    Install-WindowsFeature -Name SMTP-Server -IncludeManagementTools

    # Configure SMTP relay (customize relay host/port as needed)
    Import-Module WebAdministration
    $smtpServer = "IIS:\\\\Smtp\\\\SmtpSvc\\\\1"
    if (Test-Path $smtpServer) {
        Write-Log "Configuring SMTP relay..."
        # Set smart host if using external relay
        # Set-ItemProperty $smtpServer -Name SmartHost -Value "smtp.example.com"
        # Set-ItemProperty $smtpServer -Name SmartHostType -Value 0
        Write-Log "SMTP configuration complete. Customize relay settings as needed."
    } else {
        Write-Log "SMTP virtual server not found — manual configuration may be required."
    }
""")

